
a = int(input("Número inteiro: "))
b = int(input("Número inteiro: "))

print("%d + %d = %d" % (a, b, a + b))
print("%d - %d = %d" % (a, b, a - b))
print("%d - %d = %d" % (b, a, b - a))
print("%d * %d = %d" % (a, b, a * b))
print("%d / %d = %f" % (a, b, a / b))
print("%d / %d = %f" % (b, a, b / a))



